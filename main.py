def display_results(results):
    """
    Display visual output for both recognition paths.
    """
    if results['type'] == 'OCR':
        fig, axes = plt.subplots(1, 2, figsize=(14, 7))

        # Original image
        axes[0].imshow(cv2.cvtColor(results['original_image'], cv2.COLOR_BGR2RGB))
        axes[0].set_title("Original Image", fontsize=12)
        axes[0].axis('off')

        # Preprocessed image (binary thresholded)
        axes[1].imshow(results['preprocessed_image'], cmap='gray')
        axes[1].set_title("Preprocessed (Grayscale + Thresholding)", fontsize=12)
        axes[1].axis('off')

        plt.suptitle(f"OCR Results\nConfidence: {results['confidence']:.1%} | Text: '{results['extracted_text'][:100]}...'",
                     fontsize=14, fontweight='bold')

    else:  # Object Detection
        fig, axes = plt.subplots(1, 2, figsize=(14, 7))

        # Original image
        axes[0].imshow(cv2.cvtColor(results['original_image'], cv2.COLOR_BGR2RGB))
        axes[0].set_title("Original Image", fontsize=12)
        axes[0].axis('off')

        # Annotated image with bounding boxes
        axes[1].imshow(cv2.cvtColor(results['annotated_image'], cv2.COLOR_BGR2RGB))
        axes[1].set_title(f"Detected Objects (n={results['count']})", fontsize=12)
        axes[1].axis('off')

        detections_text = "\n".join([f"  • {d['class_name']}: {d['confidence']:.0%}"
                                      for d in results['detections'][:5]])
        plt.suptitle(f"Object Detection Results\n{results['count']} objects detected | Avg Confidence: {results['average_confidence']:.1%}\n{detections_text}",
                     fontsize=12, fontweight='bold')

    plt.tight_layout()
    plt.show()


