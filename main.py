class ObjectDetector:
    """
    Object Detection using MobileNet-SSD (Single Shot Detector)
    Implements 4D blob construction and confidence filtering
    """

    def __init__(self, model_path=None, prototxt_path=None):
        """
        Initialize MobileNet-SSD detector.

        Note: You need to download the model files:
        - deploy.prototxt: https://github.com/chuanqi305/MobileNet-SSD/blob/master/deploy.prototxt
        - model.caffemodel: https://github.com/chuanqi305/MobileNet-SSD/blob/master/deploy.caffemodel
        """
        # Use default paths (you can change these)
        self.model_path = model_path or "models/MobileNetSSD_deploy.caffemodel"
        self.prototxt_path = prototxt_path or "models/MobileNetSSD_deploy.prototxt"
        self.net = None
        self._load_model()

    def _load_model(self):
        """Load the pre-trained MobileNet-SSD model."""
        if os.path.exists(self.model_path) and os.path.exists(self.prototxt_path):
            print("   → Loading pre-trained MobileNet-SSD model...")
            self.net = cv2.dnn.readNetFromCaffe(self.prototxt_path, self.model_path)
        else:
            print("   ⚠ Warning: MobileNet-SSD model files not found.")
            print("     Using demo mode with simulated detections.")
            self.net = None

    def preprocess(self, image):
        """
        Create 4D blob from input image for SSD processing.

        The blob structure: [batch, channels, height, width]
        """
        print("   → Constructing 4D blob from image (blobFromImage)...")

        # Resize to 300x300 (MobileNet-SSD input size)
        blob = cv2.dnn.blobFromImage(image, 0.007843, (300, 300), 127.5)
        return blob

    def detect(self, image):
        """
        Detect objects in image using MobileNet-SSD.

        Args:
            image: Input image (numpy array)

        Returns:
            List of detections with class, confidence, and bounding box
        """
        if self.net is None:
            # Demo mode - return simulated detections
            return self._simulate_detections(image)

        h, w = image.shape[:2]

        # Preprocess and forward pass
        blob = self.preprocess(image)
        self.net.setInput(blob)

        print("   → Running SSD forward pass (single-shot detection)...")
        detections = self.net.forward()

        results = []
        for i in range(detections.shape[2]):
            confidence = detections[0, 0, i, 2]

            # Apply 80% confidence threshold (The Gatekeeper)
            if confidence >= CONFIDENCE_THRESHOLD:
                class_id = int(detections[0, 0, i, 1])
                class_name = SSD_CLASSES.get(class_id, f"class_{class_id}")

                # Get bounding box coordinates
                box = detections[0, 0, i, 3:7] * np.array([w, h, w, h])
                (x1, y1, x2, y2) = box.astype("int")

                results.append({
                    'class_id': class_id,
                    'class_name': class_name,
                    'confidence': confidence,
                    'bbox': (x1, y1, x2, y2)
                })

                print(f"   → Detected: {class_name} at ({x1},{y1},{x2},{y2}) with confidence {confidence:.2%}")

        return results

    def _simulate_detections(self, image):
        """Generate simulated detections for demo when model not available."""
        print("   → [DEMO MODE] Simulating object detections...")

        h, w = image.shape[:2]

        # Simulate plausible detections based on image properties
        simulated = [
            {
                'class_id': 1,
                'class_name': 'person',
                'confidence': 0.85,
                'bbox': (int(w*0.3), int(h*0.2), int(w*0.7), int(h*0.9))
            },
            {
                'class_id': 16,
                'class_name': 'cat',
                'confidence': 0.82,
                'bbox': (int(w*0.5), int(h*0.4), int(w*0.8), int(h*0.7))
            }
        ]

        filtered = [d for d in simulated if d['confidence'] >= CONFIDENCE_THRESHOLD]
        for d in filtered:
            print(f"   → [DEMO] Detected: {d['class_name']} with confidence {d['confidence']:.2%}")

        return filtered

    def draw_detections(self, image, detections):
        """
        Draw bounding boxes and labels on image.

        Args:
            image: Original image
            detections: List of detection dictionaries

        Returns:
            Annotated image
        """
        output = image.copy()

        for det in detections:
            x1, y1, x2, y2 = det['bbox']
            label = f"{det['class_name']}: {det['confidence']:.0%}"

            # Draw green bounding box (confidence >= 80%)
            cv2.rectangle(output, (x1, y1), (x2, y2), (0, 255, 0), 2)

            # Draw label background
            label_size = cv2.getTextSize(label, cv2.FONT_HERSHEY_SIMPLEX, 0.5, 2)[0]
            cv2.rectangle(output, (x1, y1 - label_size[1] - 5),
                         (x1 + label_size[0], y1), (0, 255, 0), -1)

            # Draw label text
            cv2.putText(output, label, (x1, y1 - 5),
                       cv2.FONT_HERSHEY_SIMPLEX, 0.5, (0, 0, 0), 2)

        return output

    def process_pipeline(self, image_path):
        """
        Complete object detection pipeline.

        Args:
            image_path: Path to input image

        Returns:
            Dictionary with detection results
        """
        print(f"\n[Object Detection] Processing: {image_path}")
        print("-" * 50)

        # Load image
        image = cv2.imread(image_path)
        if image is None:
            raise ValueError(f"Cannot load image from {image_path}")

        # Detect objects
        detections = self.detect(image)

        # Calculate overall metrics
        avg_confidence = np.mean([d['confidence'] for d in detections]) if detections else 0

        print(f"\n   → Total detections: {len(detections)}")
        print(f"   → Average confidence: {avg_confidence:.2%}")
        print(f"   → 80% Threshold check: {'PASSED' if any(d['confidence'] >= 0.8 for d in detections) else 'FAILED'}")

        # Draw annotations
        annotated = self.draw_detections(image, detections)

        results = {
            'type': 'Object Detection',
            'detections': detections,
            'count': len(detections),
            'average_confidence': avg_confidence,
            'threshold_passed': any(d['confidence'] >= CONFIDENCE_THRESHOLD for d in detections),
            'annotated_image': annotated,
            'original_image': image
        }

        return results


