class OCRProcessor:
    """
    Optical Character Recognition using Tesseract
    Implements: Grayscale, Gaussian Blur, Adaptive Thresholding, Deskewing
    """

    def __init__(self, psm_mode=6):
        """
        Initialize OCR processor.

        Args:
            psm_mode: Page Segmentation Mode
                3 = Fully automatic (default)
                6 = Single uniform block of text (book pages)
                7 = Single text line (number plates)
                11 = Sparse scattered text (invoices)
        """
        self.psm_mode = psm_mode
        self._configure_tesseract()

    def _configure_tesseract(self):
        """Configure Tesseract with appropriate PSM mode."""
        self.tesseract_config = f'--psm {self.psm_mode} -c tessedit_char_whitelist=ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789.,!?@#$%&*()/-:;'

    def preprocess(self, image):
        """
        Systematic image preprocessing pipeline.

        Step 1: Grayscale Conversion (Collapse 3D RGB to 1D intensity)
        Step 2: Gaussian Blur (Remove micro-imperfections)
        Step 3: Adaptive Thresholding (Binary decision for each pixel)

        Args:
            image: Input image (numpy array)

        Returns:
            Preprocessed binary image ready for OCR
        """
        print("   → Grayscale conversion (collapsing RGB matrix)...")
        gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)

        print("   → Gaussian blur (eliminating noise artifacts)...")
        blurred = cv2.GaussianBlur(gray, (5, 5), 0)

        print("   → Adaptive thresholding (forcing binary decision)...")
        # Using Otsu's method for optimal threshold calculation
        _, binary = cv2.threshold(blurred, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)

        # Alternative: Adaptive thresholding for varying lighting conditions
        # binary = cv2.adaptiveThreshold(blurred, 255, cv2.ADAPTIVE_THRESH_GAUSSIAN_C,
        #                                 cv2.THRESH_BINARY, 11, 2)

        print("   → Deskewing (correcting rotation angles)...")
        deskewed = self._deskew(binary)

        return deskewed

    def _deskew(self, image):
        """
        Calculate rotation angle and deskew the image.
        Snaps tilted text back to horizontal baseline.
        """
        coords = np.column_stack(np.where(image > 0))
        if len(coords) < 10:
            return image

        angle = cv2.minAreaRect(coords)[-1]
        if angle < -45:
            angle = -(90 + angle)
        else:
            angle = -angle

        if abs(angle) < 0.5:
            return image

        (h, w) = image.shape[:2]
        center = (w // 2, h // 2)
        M = cv2.getRotationMatrix2D(center, angle, 1.0)
        rotated = cv2.warpAffine(image, M, (w, h),
                                  flags=cv2.INTER_CUBIC,
                                  borderMode=cv2.BORDER_REPLICATE)
        return rotated

    def recognize(self, image):
        """
        Extract text from preprocessed image.

        Args:
            image: Preprocessed binary image

        Returns:
            Tuple of (extracted_text, confidence_metrics)
        """
        print("   → Running Tesseract OCR engine (CNN + bi-directional LSTM)...")

        # Get detailed data including confidence
        data = pytesseract.image_to_data(image, config=self.tesseract_config,
                                         output_type=pytesseract.Output.DICT)

        # Extract text with confidence filtering
        extracted_text = []
        confidences = []

        for i, conf in enumerate(data['conf']):
            if int(conf) >= (CONFIDENCE_THRESHOLD * 100):
                text = data['text'][i].strip()
                if text:
                    extracted_text.append(text)
                    confidences.append(conf)

        full_text = ' '.join(extracted_text)
        avg_confidence = np.mean(confidences) if confidences else 0

        return full_text, avg_confidence

    def process_pipeline(self, image_path):
        """
        Complete OCR pipeline: Preprocess → Recognize → Return results.

        Args:
            image_path: Path to input image

        Returns:
            Dictionary with recognition results
        """
        print(f"\n[OCR Pipeline] Processing: {image_path}")
        print("-" * 50)

        # Load image
        image = cv2.imread(image_path)
        if image is None:
            raise ValueError(f"Cannot load image from {image_path}")

        # Preprocess
        preprocessed = self.preprocess(image)

        # Recognize
        text, confidence = self.recognize(preprocessed)

        # Apply confidence filter (The 80% Gate)
        print(f"\n   → Confidence score: {confidence:.2f}%")
        print(f"   → 80% Threshold check: {'PASSED' if confidence >= 80 else 'FAILED'}")

        # Prepare results
        results = {
            'type': 'OCR',
            'extracted_text': text,
            'confidence': confidence / 100,  # Normalize to 0-1
            'threshold_passed': confidence >= 80,
            'preprocessed_image': preprocessed,
            'original_image': image
        }

        return results


